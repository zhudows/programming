import turtle#导入海龟工具箱
turtle.shape('turtle')
turtle.pensize(2)#笔的粗细
turtle.speed(6)#速度
turtle.pencolor('green')#笔的颜色：绿
turtle.penup()#抬笔
#一：火箭头部
turtle.goto(0,200)#去x:0 y:200(x,y坐标)
turtle.pendown()#落笔
turtle.goto(-45,100)#去x:-45 y:100(x,y坐标)
turtle.right(90)#右转90°
turtle.forward(270)#前进270(距离)
turtle.left(90)#左转90°
turtle.forward(100)#前进270(距离)
turtle.left(90)
turtle.forward(270)
turtle.goto(0,200)
turtle.penup()
#第一部分完成
#二：火箭底座
turtle.right(180)
turtle.forward(370)
turtle.right(90)
turtle.forward(20)
turtle.left(90)
turtle.pendown()
turtle.forward(15)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(15)
turtle.penup()
turtle.forward(45)
turtle.left(90)
turtle.forward(75)
turtle.pendown()
turtle.left(45)
turtle.forward(75)
turtle.left(45)
turtle.forward(35)
turtle.left(135)
turtle.forward(75)
turtle.penup()
turtle.right(45)
turtle.forward(100)
turtle.right(45)
turtle.pendown()
turtle.forward(75)
turtle.left(135)
turtle.forward(35)
turtle.left(45)
turtle.forward(75)
turtle.penup()
turtle.done()