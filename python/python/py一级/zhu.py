import turtle
turtle.shape('turtle')
turtle.screensize(400,400)
turtle.speed(5)
turtle.pencolor('blue')
turtle.pensize(7)
turtle.penup()
turtle.goto(-80,50)
turtle.pendown()
turtle.circle(40)
turtle.pencolor('black')
turtle.penup()
turtle.goto(0,50)
turtle.pendown()
turtle.pencolor('black')
turtle.circle(40)
turtle.penup()
turtle.goto(80,50)
turtle.pendown()
turtle.pencolor('red')
turtle.circle(40)
turtle.penup()
turtle.goto(-40,0)
turtle.pendown()
turtle.pencolor('yellow')
turtle.circle(40)
turtle.penup()
turtle.goto(40,0)
turtle.pendown()
turtle.pencolor('green')
turtle.circle(40)
turtle.penup()
turtle.done()