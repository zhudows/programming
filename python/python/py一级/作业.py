import turtle
turtle.shape('turtle')
turtle.pencolor('blue')
turtle.pensize(7)
turtle.speed(4)
turtle.penup()
turtle.goto(-100,50)
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.pencolor('black')
turtle.goto(-20,50)
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.pencolor('red')
turtle.goto(60,50)
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.pencolor('yellow')
turtle.goto(-50,0)
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.pencolor('green')
turtle.goto(20,0)
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.done()