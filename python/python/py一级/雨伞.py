import turtle
turtle.shape('turtle')
turtle.pencolor('green')
turtle.pensize(4)
turtle.speed(0)
turtle.fillcolor('green')
turtle.begin_fill()
turtle.circle(-30,90)
turtle.circle(30,-180)
turtle.circle(90,180)
turtle.circle(30,-180)
turtle.circle(-30,90)
turtle.end_fill()
turtle.left(-90)
turtle.forward(150)
turtle.circle(-15,180)
turtle.done()