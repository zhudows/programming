import turtle
turtle.shape('turtle')
turtle.pencolor('green')
turtle.fillcolor('green')
turtle.pensize(4)
turtle.speed(0)
turtle.penup()
turtle.goto(0,-150)
turtle.pendown()
turtle.circle(150)
turtle.circle(75,180)
turtle.circle(-75,180)
turtle.penup()
turtle.seth(-90)
turtle.forward(60)
turtle.pendown()
turtle.begin_fill()
turtle.circle(10)
turtle.penup()
turtle.forward(150)
turtle.pendown()
turtle.circle(10)
turtle.end_fill()
turtle.penup()
turtle.goto(0,0)
turtle.done()