import turtle
turtle.shape('turtle')
turtle.speed(1)
turtle.pensize(3)
turtle.pencolor('black')
turtle.fillcolor('red')
turtle.penup()
turtle.seth(90)
turtle.forward(75)
turtle.seth(180)
turtle.forward(75)
turtle.seth(0)
turtle.pendown()
turtle.forward(150)
turtle.begin_fill()
turtle.seth(-135)
turtle.forward(215)
turtle.seth(90)
turtle.forward(150)
turtle.end_fill()
turtle.penup()
turtle.seth(-90)
turtle.forward(150)
turtle.seth(0)
turtle.pendown()
turtle.fillcolor('yellow')
turtle.begin_fill()
turtle.forward(150)
turtle.seth(90)
turtle.forward(150)
turtle.seth(90)
turtle.penup()
turtle.end_fill()
turtle.goto(0,200)
turtle.pencolor('green')
turtle.write('测试已完成',font=('微软雅黑',20,'normal'),align='center')
turtle.hideturtle()
turtle.done()