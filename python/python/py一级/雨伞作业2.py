import turtle
turtle.shape('turtle')
turtle.fillcolor('green')
turtle.pensize(3)
turtle.pencolor('green')
turtle.begin_fill()
turtle.seth(0)
turtle.circle(-30,90)
turtle.seth(90)
turtle.circle(-30,180)
turtle.seth(90)
turtle.circle(90,180)
turtle.seth(90)
turtle.circle(-30,180)
turtle.seth(90)
turtle.circle(-30,90)
turtle.end_fill()
turtle.seth(-90)
turtle.forward(145)
turtle.circle(-15,180)
turtle.done()