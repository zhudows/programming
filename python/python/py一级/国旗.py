import turtle
turtle.shape('turtle')
turtle.pensize(3)
turtle.pencolor('red')
turtle.fillcolor('red')
turtle.speed(1)
turtle.penup()
turtle.begin_fill()
turtle.goto(-200,125)
turtle.pendown()
turtle.goto(200,125)
turtle.goto(200,-150)
turtle.goto(-200,-150)
turtle.goto(-200,125)
turtle.end_fill()
turtle.pencolor('yellow')
turtle.penup()
turtle.goto(-125,70)
turtle.right(108)
turtle.pendown()
turtle.fillcolor('yellow')
turtle.begin_fill()
for i in range(5):
    turtle.forward(60)
    turtle.left(144)
turtle.end_fill()
turtle.penup()
