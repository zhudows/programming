import turtle
turtle.shape('turtle')
turtle.pensize(3)
turtle.pencolor('black')
turtle.fillcolor('yellow')
turtle.speed(7)
turtle.seth(90)
turtle.penup()
turtle.forward(150)
turtle.seth(0)
turtle.pendown()
turtle.circle(-150)
turtle.penup()
turtle.goto(0,20)
turtle.pendown()
turtle.circle(-40,steps=3)
turtle.penup()
turtle.seth(180)
turtle.forward(100)
turtle.seth(-90)
turtle.forward(55)
turtle.left(60)
turtle.pendown()
turtle.forward(110)
turtle.left(60)
turtle.forward(110)
turtle.penup()
turtle.goto(-50,50)
turtle.seth(0)
turtle.pendown()
turtle.fillcolor('green')
turtle.begin_fill()
turtle.circle(20)
turtle.end_fill()
turtle.penup()
turtle.goto(50,50)
turtle.pendown()
turtle.begin_fill()
turtle.circle(20)
turtle.end_fill()
turtle.hideturtle()
turtle.done()
